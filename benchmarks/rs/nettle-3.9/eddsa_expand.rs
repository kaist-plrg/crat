pub unsafe extern "C" fn _nettle_eddsa_expand_key(
    mut ecc: *const ecc_curve,
    mut eddsa: *const ecc_eddsa,
    mut ctx: *mut libc::c_void,
    mut key: *const uint8_t,
    mut digest: *mut uint8_t,
    mut k2: *mut mp_limb_t,
) {
    let mut nbytes: size_t = (1 as libc::c_int
        + (*ecc).p.bit_size as libc::c_int / 8 as libc::c_int) as size_t;
    ((*eddsa).update).unwrap()(ctx, nbytes, key);
    ((*eddsa).digest)
        .unwrap()(ctx, (2 as libc::c_int as libc::c_ulong).wrapping_mul(nbytes), digest);
    _nettle_mpn_set_base256_le(
        k2,
        (*ecc).p.size as mp_size_t,
        digest,
        (((*ecc).p.bit_size as libc::c_int + 7 as libc::c_int) / 8 as libc::c_int)
            as size_t,
    );
    let ref mut fresh0 = *k2.offset(0 as libc::c_int as isize);
    *fresh0 &= (*eddsa).low_mask;
    let ref mut fresh1 = *k2
        .offset(((*ecc).p.size as libc::c_int - 1 as libc::c_int) as isize);
    *fresh1 &= ((*eddsa).high_bit).wrapping_sub(1 as libc::c_int as libc::c_ulong);
    let ref mut fresh2 = *k2
        .offset(((*ecc).p.size as libc::c_int - 1 as libc::c_int) as isize);
    *fresh2 |= (*eddsa).high_bit;
}
