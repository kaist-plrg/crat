unsafe extern "C" fn recordCallback(
    mut inputBuffer: *const libc::c_void,
    mut outputBuffer: *mut libc::c_void,
    mut framesPerBuffer: libc::c_ulong,
    mut timeInfo: *const PaStreamCallbackTimeInfo,
    mut statusFlags: PaStreamCallbackFlags,
    mut userData: *mut libc::c_void,
) -> libc::c_int {
    let mut data: *mut paTestData = userData as *mut paTestData;
    let mut rptr: *mut SAMPLE = inputBuffer as *mut SAMPLE;
    let mut framesToCalc: libc::c_long = 0;
    let mut finished: libc::c_int = 0;
    let mut framesLeft: libc::c_ulong = ((*data).maxFrameIndex - (*data).frameIndex)
        as libc::c_ulong;
    if framesLeft < framesPerBuffer {
        framesToCalc = framesLeft as libc::c_long;
        finished = paComplete as libc::c_int;
    } else {
        framesToCalc = framesPerBuffer as libc::c_long;
        finished = paContinue as libc::c_int;
    }
    if inputBuffer.is_null() {
        write_to_cava_input_buffers(
            (framesToCalc * 2 as libc::c_int as libc::c_long) as int16_t,
            silence_buffer.as_mut_ptr(),
            audio as *mut libc::c_void,
        );
    } else {
        write_to_cava_input_buffers(
            (framesToCalc * 2 as libc::c_int as libc::c_long) as int16_t,
            rptr,
            audio as *mut libc::c_void,
        );
    }
    (*data)
        .frameIndex = ((*data).frameIndex as libc::c_long + framesToCalc) as libc::c_int;
    if finished == paComplete as libc::c_int {
        (*data).frameIndex = 0 as libc::c_int;
        finished = paContinue as libc::c_int;
    }
    if (*audio).terminate == 1 as libc::c_int {
        finished = paComplete as libc::c_int;
    }
    return finished;
}
