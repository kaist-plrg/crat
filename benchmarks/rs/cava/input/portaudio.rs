pub unsafe extern "C" fn input_portaudio(
    mut audiodata: *mut libc::c_void,
) -> *mut libc::c_void {
    audio = audiodata as *mut audio_data;
    let mut inputParameters: PaStreamParameters = PaStreamParameters {
        device: 0,
        channelCount: 0,
        sampleFormat: 0,
        suggestedLatency: 0.,
        hostApiSpecificStreamInfo: 0 as *mut libc::c_void,
    };
    let mut stream: *mut libc::c_void = 0 as *mut libc::c_void;
    let mut err: PaError = paNoError as libc::c_int;
    let mut data: paTestData = paTestData {
        frameIndex: 0,
        maxFrameIndex: 0,
        recordedSamples: 0 as *mut SAMPLE,
    };
    err = Pa_Initialize();
    if err != paNoError as libc::c_int {
        fprintf(
            stderr,
            b"Error: unable to initilize portaudio - %s\n\0" as *const u8
                as *const libc::c_char,
            Pa_GetErrorText(err),
        );
        exit(1 as libc::c_int);
    }
    let mut deviceNum: libc::c_int = -(1 as libc::c_int);
    let mut numOfDevices: libc::c_int = Pa_GetDeviceCount();
    if strcmp((*audio).source, b"list\0" as *const u8 as *const libc::c_char) == 0 {
        if numOfDevices < 0 as libc::c_int {
            fprintf(
                stderr,
                b"Error: portaudio was unable to find a audio device! Code: 0x%x\n\0"
                    as *const u8 as *const libc::c_char,
                numOfDevices,
            );
            exit(1 as libc::c_int);
        }
        let mut i: libc::c_int = 0 as libc::c_int;
        while i < numOfDevices {
            let mut deviceInfo: *const PaDeviceInfo = Pa_GetDeviceInfo(i);
            printf(
                b"Device #%d: %s\n\tInput Channels: %d\n\tOutput Channels: %d\n\tDefault SampleRate: %lf\n\0"
                    as *const u8 as *const libc::c_char,
                i + 1 as libc::c_int,
                (*deviceInfo).name,
                (*deviceInfo).maxInputChannels,
                (*deviceInfo).maxOutputChannels,
                (*deviceInfo).defaultSampleRate,
            );
            i += 1;
            i;
        }
        exit(0 as libc::c_int);
    } else if strcmp((*audio).source, b"auto\0" as *const u8 as *const libc::c_char) == 0
    {
        deviceNum = Pa_GetDefaultInputDevice();
        if deviceNum == -(1 as libc::c_int) {
            fprintf(
                stderr,
                b"Error: no portaudio input device found\n\0" as *const u8
                    as *const libc::c_char,
            );
            exit(1 as libc::c_int);
        }
    } else if sscanf(
        (*audio).source,
        b"%d\0" as *const u8 as *const libc::c_char,
        &mut deviceNum as *mut libc::c_int,
    ) != 0
    {
        if deviceNum > numOfDevices {
            fprintf(
                stderr,
                b"Error: Invalid input device!\n\0" as *const u8 as *const libc::c_char,
            );
            exit(1 as libc::c_int);
        }
        deviceNum -= 1;
        deviceNum;
    } else {
        let mut i_0: libc::c_int = 0 as libc::c_int;
        while i_0 < numOfDevices {
            let mut deviceInfo_0: *const PaDeviceInfo = Pa_GetDeviceInfo(i_0);
            if strcmp((*audio).source, (*deviceInfo_0).name) == 0 {
                deviceNum = i_0;
                break;
            } else {
                i_0 += 1;
                i_0;
            }
        }
        if deviceNum == -(1 as libc::c_int) {
            fprintf(
                stderr,
                b"Error: No such device '%s'!\n\0" as *const u8 as *const libc::c_char,
                (*audio).source,
            );
            exit(1 as libc::c_int);
        }
    }
    inputParameters.device = deviceNum;
    data
        .maxFrameIndex = (*audio).input_buffer_size * 1024 as libc::c_int
        / 2 as libc::c_int;
    data
        .recordedSamples = malloc(
        ((2 as libc::c_int * data.maxFrameIndex) as libc::c_ulong)
            .wrapping_mul(::std::mem::size_of::<SAMPLE>() as libc::c_ulong),
    ) as *mut SAMPLE;
    if (data.recordedSamples).is_null() {
        fprintf(
            stderr,
            b"Error: failure in memory allocation!\n\0" as *const u8
                as *const libc::c_char,
        );
        exit(1 as libc::c_int);
    } else {
        memset(
            data.recordedSamples as *mut libc::c_void,
            0 as libc::c_int,
            (2 as libc::c_int * data.maxFrameIndex) as libc::c_ulong,
        );
    }
    inputParameters.channelCount = 2 as libc::c_int;
    inputParameters.sampleFormat = 0x8 as libc::c_int as PaSampleFormat;
    inputParameters
        .suggestedLatency = (*Pa_GetDeviceInfo(inputParameters.device))
        .defaultLowInputLatency;
    inputParameters.hostApiSpecificStreamInfo = 0 as *mut libc::c_void;
    err = Pa_OpenStream(
        &mut stream,
        &mut inputParameters,
        0 as *const PaStreamParameters,
        (*audio).rate as libc::c_double,
        ((*audio).input_buffer_size / 2 as libc::c_int) as libc::c_ulong,
        0x1 as libc::c_int as PaStreamFlags,
        Some(
            recordCallback
                as unsafe extern "C" fn(
                    *const libc::c_void,
                    *mut libc::c_void,
                    libc::c_ulong,
                    *const PaStreamCallbackTimeInfo,
                    PaStreamCallbackFlags,
                    *mut libc::c_void,
                ) -> libc::c_int,
        ),
        &mut data as *mut paTestData as *mut libc::c_void,
    );
    if err != paNoError as libc::c_int {
        fprintf(
            stderr,
            b"Error: failure in opening stream (%x)\n\0" as *const u8
                as *const libc::c_char,
            err,
        );
        exit(1 as libc::c_int);
    }
    loop {
        data.frameIndex = 0 as libc::c_int;
        err = Pa_StartStream(stream);
        if err != paNoError as libc::c_int {
            fprintf(
                stderr,
                b"Error: failure in starting stream (%x)\n\0" as *const u8
                    as *const libc::c_char,
                err,
            );
            exit(1 as libc::c_int);
        }
        loop {
            err = Pa_IsStreamActive(stream);
            if !(err == 1 as libc::c_int) {
                break;
            }
            Pa_Sleep(1000 as libc::c_int as libc::c_long);
            if (*audio).terminate == 1 as libc::c_int {
                break;
            }
        }
        if err < 0 as libc::c_int {
            fprintf(
                stderr,
                b"Error: failure in recording audio (%x)\n\0" as *const u8
                    as *const libc::c_char,
                err,
            );
            exit(1 as libc::c_int);
        }
        if (*audio).terminate == 1 as libc::c_int {
            break;
        }
    }
    err = Pa_CloseStream(stream);
    if err != paNoError as libc::c_int {
        fprintf(
            stderr,
            b"Error: failure in closing stream (%x)\n\0" as *const u8
                as *const libc::c_char,
            err,
        );
        exit(1 as libc::c_int);
    }
    portaudio_simple_free(data);
    return 0 as *mut libc::c_void;
}
